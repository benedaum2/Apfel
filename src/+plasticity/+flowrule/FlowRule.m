classdef FlowRule < handle

  
%===============================================================================
properties (SetAccess = protected)

end  


%===============================================================================
methods (Access = public)
%-------------------------------------------------------------------------------
% Constructor
  function self = FlowRule(data)
  end
%-------------------------------------------------------------------------------
  function f = getF(self, sigma)
    f       = sigma'*self.getMatrix*sigma - 1;
  end
%-------------------------------------------------------------------------------
  function sigma_v = getSigmaV(self, sigma)
    sigma_v = sigma'*self.getMatrix*sigma;
  end
%-------------------------------------------------------------------------------
end

%===============================================================================
methods (Abstract, Access = public)
  getMatrix(self)
end
%-------------------------------------------------------------------------------

end